def count_words_in_pair(gov_keywords_pair)
  hash = Hash.new(0)

  gov_keywords_pair.each do |phrase|
    phrase.split(" ").each { |word| hash[word] += 1 }
  end

  hash.sort_by { |k, v| -v }.each { |pair| puts "'#{pair.first}'," }
end

def identify_duplicates(one_word_set, one_from_pair_words)
  one_from_pair_words.each do |word|
    if one_word_set.include?(word)
      puts "'#{word}',"
    end
  end
end

def identify_uniques(one_word_set, one_from_pair_words)
  one_from_pair_words.each do |word|
    if !one_word_set.include?(word)
      puts "'#{word}',"
    end
  end
end

def remove_duplicates(duplicate_words, two_word_set)
  puts two_word_set.length

  two_word_set.each do |word_pair|
    word_pair.split(" ").each do |word|
      if duplicate_words.include?(word)
        two_word_set.delete(word_pair)
      end
    end
  end

  two_word_set.each { |word_pair| puts "'#{word_pair}',"}

  puts two_word_set.length
end

